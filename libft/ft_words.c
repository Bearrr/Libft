#include "libft.h"

int			ft_words(const char *s, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!s && !c)
		return (0);
	while (s[i] == c && s[i])
		i++;
	while (s[i])
	{
		while (s[i] != c && s[i])
			i++;
		j++;
		while (s[i] == c && s[i])
			i++;
	}
	return (j);
}
