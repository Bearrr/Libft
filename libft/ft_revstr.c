#include "libft.h"

char		*ft_revstr(char *s)
{
	size_t	i;
	char	j;
	size_t	c;

	if (!s)
		return (NULL);
	i = 0;
	c = ft_strlen(s) - 1;
	while (i < c)
	{
		j = s[i];
		s[i] = s[c];
		s[c] = j;
		i++;
		c--;
	}
	return (s);
}
